"use server"

import { pool } from "@/app/api/db"
import querys from "@/app/api/querys"

export async function addIdiomas(idiomas, id_usuario) {
    try {
        const valuesString = idiomas
            .map(h => `(${id_usuario}, ${h.value})`)
            .join(", ");

        await pool.query(`${querys.addIdiomas} ${valuesString}`);
        return {
            ok: true,
            message: "Idiomas actualizados"
        };
    } catch (error) {
        console.log(error);

        if (error.code === "ECONNREFUSED") {
            return {
                ok: false,
                message: "No se pudo conectar a la base de datos. Por favor intenta m치s tarde.",
            };
        }

        if (error.code === "ETIMEDOUT") {
            return {
                ok: false,
                message: "La conexi칩n a la base de datos expir칩 (timeout). Intenta de nuevo m치s tarde.",
            };
        }

        return {
            ok: false,
            message: "Ocurrio un error."
        }
    }
}